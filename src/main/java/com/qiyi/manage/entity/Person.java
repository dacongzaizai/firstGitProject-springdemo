package com.qiyi.manage.entity;

public class Person {

private Long f_id;
	
	private String f_user_name;
	
	private String f_user_no;

	public Long getF_id() {
		return f_id;
	}

	public void setF_id(Long f_id) {
		this.f_id = f_id;
	}

	public String getF_user_name() {
		return f_user_name;
	}

	public void setF_user_name(String f_user_name) {
		this.f_user_name = f_user_name;
	}

	public String getF_user_no() {
		return f_user_no;
	}

	public void setF_user_no(String f_user_no) {
		this.f_user_no = f_user_no;
	}
}
